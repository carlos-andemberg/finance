function miniChart(prices, currentIdx, width = 80, height = 30) {
  const start = Math.max(0, currentIdx - 11);
  const slice = prices.slice(start, currentIdx + 1);
  if (slice.length < 2) return '<svg></svg>';
  const min = Math.min(...slice);
  const max = Math.max(...slice);
  const range = max - min || 1;
  const pts = slice.map((p, i) => {
    const x = (i / (slice.length - 1)) * width;
    const y = height - ((p - min) / range) * height;
    return `${x.toFixed(1)},${y.toFixed(1)}`;
  }).join(' ');
  const color = slice[slice.length-1] >= slice[0] ? '#00d084' : '#ff4757';
  return `<svg width="${width}" height="${height}" viewBox="0 0 ${width} ${height}">
    <polyline points="${pts}" fill="none" stroke="${color}" stroke-width="1.5" stroke-linecap="round" stroke-linejoin="round"/>
  </svg>`;
}

