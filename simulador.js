function getAllAssets() {
  return [...CATALOG.acoes, ...CATALOG.tesouro, ...CATALOG.fixa];
}

function getAsset(id) {
  return getAllAssets().find(a => a.id === id);
}

function currentPrice(id) {
  return getAsset(id)?.prices[state.month] ?? 0;
}

function previousPrice(id) {
  if (state.month === 0) return currentPrice(id);
  return getAsset(id)?.prices[state.month - 1] ?? currentPrice(id);
}

function monthChange(id) {
  const cur = currentPrice(id);
  const prev = previousPrice(id);
  return ((cur - prev) / prev) * 100;
}

function calcPatrimony() {
  let pat = state.balance;
  for (const [id, pos] of Object.entries(state.portfolio)) {
    pat += pos.qty * currentPrice(id);
  }
  return pat;
}

function calcInvested() {
  let total = 0;
  for (const pos of Object.values(state.portfolio)) total += pos.totalInvested;
  return total;
}

function calcCurrentPortfolio() {
  let total = 0;
  for (const [id, pos] of Object.entries(state.portfolio)) total += pos.qty * currentPrice(id);
  return total;
}

function yearMonth(idx) {
  const year = 2010 + Math.floor(idx / 12);
  return { year, month: idx % 12 };
}

function dateLabel(idx) {
  const { year, month } = yearMonth(idx);
  return `${MONTHS[month]} ${year}`;
}

function salary(idx) {
  const { year } = yearMonth(idx);
  return SALARY_BY_YEAR[year] ?? 510;
}

