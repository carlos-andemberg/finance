let lastFocusedElement = null;

function openBuyPanel(assetId) {
  lastFocusedElement = document.activeElement;
  state.selectedAsset = assetId;
  render();
  const a = getAsset(assetId);
  const price = currentPrice(assetId);
  document.getElementById('buy-panel-name').textContent = `${a.id} — ${a.name}`;
  document.getElementById('buy-panel-price').textContent = `R$ ${fmt.brl(price)} por unidade`;
  const qtyInput = document.getElementById('buy-qty');
  qtyInput.value = '1';
  qtyInput.dataset.pristine = 'true';
  document.getElementById('buy-panel').classList.add('visible');
  updateBuyTotal();
  if (!document.body.classList.contains('tutorial-active') && window.innerWidth > 900) {
    qtyInput.focus();
  }
}

function closeBuyPanel() {
  state.selectedAsset = null;
  document.getElementById('buy-panel').classList.remove('visible');
  render();
  if (lastFocusedElement) {
    lastFocusedElement.focus();
    lastFocusedElement = null;
  }
}

function updateBuyTotal() {
  const id = state.selectedAsset;
  if (!id) return;
  const qty   = parseInt(document.getElementById('buy-qty').value) || 0;
  const price = currentPrice(id);
  const total = qty * price;
  document.getElementById('buy-total').textContent = `R$ ${fmt.brl(total)}`;
  const canBuy  = total <= state.balance && qty > 0;
  const canSell = (state.portfolio[id]?.qty ?? 0) >= qty && qty > 0;
  document.getElementById('buy-warn').style.display = (qty > 0 && !canBuy) ? 'block' : 'none';
  document.getElementById('btn-confirm-buy').disabled  = !canBuy;
  document.getElementById('btn-confirm-sell').disabled = !canSell;
}

function confirmBuy() {
  const id  = state.selectedAsset;
  const qty = parseInt(document.getElementById('buy-qty').value) || 0;
  if (!id || qty <= 0) return;
  const price = currentPrice(id);
  const total = qty * price;
  if (total > state.balance) return;
  state.balance -= total;
  if (!state.portfolio[id]) state.portfolio[id] = { qty: 0, totalInvested: 0, avgPrice: price };
  const pos = state.portfolio[id];
  const prevTotal = pos.qty * pos.avgPrice;
  pos.qty += qty;
  pos.totalInvested += total;
  pos.avgPrice = (prevTotal + total) / pos.qty;
  saveState();
  render();
  showToast(`✅ Compra de ${qty} ${id} @ R$ ${fmt.brl(price)}`, 'buy');
}

function confirmSell() {
  const id  = state.selectedAsset;
  const qty = parseInt(document.getElementById('buy-qty').value) || 0;
  if (!id || qty <= 0) return;
  const pos = state.portfolio[id];
  if (!pos || pos.qty < qty) return;
  const price = currentPrice(id);
  state.balance += qty * price;
  pos.qty -= qty;
  pos.totalInvested -= qty * pos.avgPrice;
  if (pos.qty <= 0) delete state.portfolio[id];
  saveState();
  render();
  showToast(`📤 Venda de ${qty} ${id} @ R$ ${fmt.brl(price)}`, 'buy');
}

