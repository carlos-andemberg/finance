function advanceMonth() {
  if (state.isFinished || state.month >= TOTAL_MONTHS - 1) {
    showFinalResult();
    return;
  }

  // Calcular dividendos do mês que fecha
  let divReport = [];
  let divTotal = 0;
  for (const [id, pos] of Object.entries(state.portfolio)) {
    const d = getAsset(id)?.divs[state.month] ?? 0;
    if (d > 0 && pos.qty > 0) {
      const received = d * pos.qty;
      divTotal += received;
      divReport.push({ id, received });
      pos.divsReceived = (pos.divsReceived || 0) + received;
    }
  }
  state.balance += divTotal;
  state.totalDivs += divTotal;

  // Avança o mês
  state.month += 1;

  // Injeta novo salário
  const sal = salary(state.month);
  state.balance += sal;

  saveState();
  showMonthModal(divTotal, divReport, sal);
  render();
}

