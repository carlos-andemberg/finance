let tutCurrentStep = 0;
let tutActiveTarget = null;

const tutOverlay = document.getElementById('tutorial-overlay');
const tutPopup = document.getElementById('tutorial-popup');
const tutTitle = document.getElementById('tut-title');
const tutText = document.getElementById('tut-text');
const tutArrow = document.getElementById('tut-arrow');
const tutDotsContainer = document.getElementById('tut-dots');
const btnTutPrev = document.getElementById('tut-prev');
const btnTutNext = document.getElementById('tut-next');
const btnTutSkip = document.getElementById('tut-skip');

function initTutorialUI() {
  tutDotsContainer.innerHTML = tutorialSteps.map((_, i) => `<div class="tut-dot" data-step="${i}"></div>`).join('');
  
  btnTutPrev.addEventListener('click', () => { if (tutCurrentStep > 0) showTutorialStep(tutCurrentStep - 1); });
  btnTutNext.addEventListener('click', () => {
    if (tutCurrentStep < tutorialSteps.length - 1) {
      showTutorialStep(tutCurrentStep + 1);
    } else {
      finishTutorial();
    }
  });
  btnTutSkip.addEventListener('click', finishTutorial);
  
  const btnRestart = document.getElementById('btn-restart-tutorial');
  if (btnRestart) {
    btnRestart.addEventListener('click', () => {
      showTutorialStep(0);
    });
  }
}

function initTutorial() {
  initTutorialUI();
  if (localStorage.getItem('tutFinished')) return;
  // Wait a bit before showing to let UI render properly
  setTimeout(() => showTutorialStep(0), 500);
}

function showTutorialStep(index) {
  tutCurrentStep = index;
  const step = tutorialSteps[index];
  
  let targetSelector = step.target;
  
  // Prevent scroll during tutorial
  document.body.classList.add('tutorial-active');
  const main = document.querySelector('.broker-main');
  if (main) main.classList.add('tutorial-active');

  // Cleanup previous target z-index and pointer-events
  if (window.tutPrevTarget) {
    window.tutPrevTarget.style.zIndex = '';
    
    if (window.tutPrevTarget.dataset.tutOldPos) {
      window.tutPrevTarget.style.position = window.tutPrevTarget.dataset.tutOldPos;
      delete window.tutPrevTarget.dataset.tutOldPos;
    } else {
      window.tutPrevTarget.style.position = '';
    }
    
    if (window.tutPrevTarget.dataset.tutOldPointer !== undefined) {
      window.tutPrevTarget.style.pointerEvents = window.tutPrevTarget.dataset.tutOldPointer;
      delete window.tutPrevTarget.dataset.tutOldPointer;
    } else {
      window.tutPrevTarget.style.pointerEvents = '';
    }
  }

  tutActiveTarget = document.querySelector(targetSelector);
  if (!tutActiveTarget) {
    console.warn('Tutorial target not found:', targetSelector);
    return;
  }

  // Elevate target above overlay and other sticky elements
  window.tutPrevTarget = tutActiveTarget;
  const currentPos = window.getComputedStyle(tutActiveTarget).position;
  if (currentPos === 'static') {
    tutActiveTarget.dataset.tutOldPos = tutActiveTarget.style.position;
    tutActiveTarget.style.position = 'relative';
  }
  tutActiveTarget.style.zIndex = '10000';
  
  // Disable interactions on the target
  tutActiveTarget.dataset.tutOldPointer = tutActiveTarget.style.pointerEvents;
  tutActiveTarget.style.pointerEvents = 'none';
  
  // 1. BUY PANEL MOCKING
  const buyPanel = document.getElementById('buy-panel');
  if (targetSelector === '#buy-panel') {
    if (typeof state !== 'undefined' && !state.selectedAsset && !buyPanel.dataset.tutMock) {
      buyPanel.dataset.tutMock = 'true';
      openBuyPanel('VALE3'); // Sets state, opens panel, calls render()
      // Blur the input to prevent mobile keyboard from opening
      const qtyInput = document.getElementById('buy-qty');
      if (qtyInput) qtyInput.blur();
    } else {
      buyPanel.classList.add('visible');
    }
  } else if (buyPanel) {
    if (buyPanel.dataset.tutMock === 'true') {
      delete buyPanel.dataset.tutMock;
      closeBuyPanel(); // Cleans state, closes panel, calls render()
    }
    if (typeof state !== 'undefined' && !state.selectedAsset) {
      buyPanel.classList.remove('visible');
    }
  }

  // 2. PORTFOLIO MOCKING
  const portSummary = document.getElementById('portfolio-summary');
  const isPortfolioStep = targetSelector === '#portfolio-summary' || targetSelector === 'aside.portfolio-panel' || targetSelector === '#btn-show-report';
  
  if (isPortfolioStep) {
    if (typeof state !== 'undefined' && Object.keys(state.portfolio || {}).length === 0 && !document.body.dataset.tutPortMock) {
      document.body.dataset.tutPortMock = 'true';
      state.portfolio['VALE3'] = { qty: 100, avgPrice: 48.91, totalInvested: 4891.00, totalDivs: 0 }; 
      if (typeof renderPortfolio === 'function') renderPortfolio();
    }
    if (targetSelector === '#btn-show-report' && portSummary) {
      portSummary.style.display = 'block';
      portSummary.dataset.tutForced = 'true';
    }
  } else {
    // Cleanup portfolio mock if we leave portfolio steps
    if (document.body.dataset.tutPortMock === 'true') {
      delete document.body.dataset.tutPortMock;
      delete state.portfolio['VALE3'];
      if (typeof renderPortfolio === 'function') renderPortfolio();
    }
    if (portSummary && portSummary.dataset.tutForced === 'true') {
      if (typeof state !== 'undefined' && Object.keys(state.portfolio || {}).length === 0) {
        portSummary.style.display = 'none';
      }
      delete portSummary.dataset.tutForced;
    }
  }

  const finalOverlay = document.getElementById('final-overlay');
  if (step.target === '.timeline-section' || step.target === '#btn-restart-final') {
    if (finalOverlay && !finalOverlay.classList.contains('active')) {
      finalOverlay.classList.add('active');
      finalOverlay.dataset.tutForced = 'true';
    }
  } else if (finalOverlay && finalOverlay.dataset.tutForced === 'true') {
    finalOverlay.classList.remove('active');
    delete finalOverlay.dataset.tutForced;
  }
  
  tutOverlay.classList.remove('hidden');
  tutPopup.classList.remove('hidden');
  
  // Allow DOM to update (e.g. display: block) before scrolling
  const scrollToTarget = () => {
    if (window.innerWidth < 900 && step.target === 'aside.portfolio-panel') {
      tutActiveTarget.scrollIntoView({ behavior: 'auto', block: 'start' });
    } else {
      tutActiveTarget.scrollIntoView({ behavior: 'auto', block: 'center' });
    }
  };

  setTimeout(() => {
    scrollToTarget();
    // A tiny delay to ensure the scroll position is fully applied before calculating rect
    requestAnimationFrame(() => {
      positionTutorialPopup(step);
    });
  }, 100);
  
  tutTitle.textContent = step.title;
  tutText.textContent = step.text;
  
  // Update dots
  Array.from(tutDotsContainer.children).forEach((dot, i) => {
    dot.classList.toggle('active', i === index);
  });
  
  btnTutPrev.disabled = (index === 0);
  btnTutNext.textContent = (index === tutorialSteps.length - 1) ? 'Começar!' : 'Próximo ➔';
}

function positionTutorialPopup(step) {
  if (!tutActiveTarget) return;
  const rect = tutActiveTarget.getBoundingClientRect();
  
  // Position the hole
  const tutHole = document.getElementById('tutorial-hole');
  if (tutHole) {
    const pad = 6;
    tutHole.style.top = (rect.top - pad) + 'px';
    tutHole.style.left = (rect.left - pad) + 'px';
    tutHole.style.width = (rect.width + pad * 2) + 'px';
    tutHole.style.height = (rect.height + pad * 2) + 'px';
  }
  
  let top = 0;
  let left = 0;
  let arrowTop = '';
  let arrowLeft = '';
  
  tutPopup.style.position = 'fixed';
  
  const popupRect = tutPopup.getBoundingClientRect();
  const gap = 16;
  const placement = window.innerWidth < 768 ? 'bottom' : step.placement; 
  
  if (placement === 'bottom') {
    top = rect.bottom + gap;
    left = rect.left + (rect.width / 2) - (popupRect.width / 2);
    arrowTop = '-8px';
    arrowLeft = 'calc(50% - 7px)';
  } else if (placement === 'top') {
    top = rect.top - popupRect.height - gap;
    left = rect.left + (rect.width / 2) - (popupRect.width / 2);
    arrowTop = 'calc(100% - 7px)';
    arrowLeft = 'calc(50% - 7px)';
  } else if (placement === 'left') {
    top = rect.top + (rect.height / 2) - (popupRect.height / 2);
    left = rect.left - popupRect.width - gap;
    arrowTop = 'calc(50% - 7px)';
    arrowLeft = 'calc(100% - 7px)';
  } else if (placement === 'right') {
    top = rect.top + (rect.height / 2) - (popupRect.height / 2);
    left = rect.right + gap;
    arrowTop = 'calc(50% - 7px)';
    arrowLeft = '-8px';
  }
  
  const margin = 10;
  const minTopMargin = window.innerWidth < 900 ? 95 : 10;
  
  // General placement overflow fixes
  if (top + popupRect.height > window.innerHeight - margin) {
    // If it overflows bottom, force top
    if (placement === 'bottom' || placement === 'left' || placement === 'right') {
      top = rect.top - popupRect.height - gap;
      arrowTop = 'calc(100% - 7px)';
    }
  }

  if (top < minTopMargin) {
    // If it overflows top, force bottom
    if (placement === 'top') {
      top = rect.bottom + gap;
      arrowTop = '-8px';
    }
  }

  // Final clamp to screen bounds (just in case)
  if (left < margin) left = margin;
  if (left + popupRect.width > window.innerWidth - margin) {
    left = window.innerWidth - popupRect.width - margin;
  }
  if (top < minTopMargin) top = minTopMargin;
  if (top + popupRect.height > window.innerHeight - margin) {
    top = window.innerHeight - popupRect.height - margin;
  }

  // Adjust arrow dynamically based on clamped positions
  if (arrowTop === '-8px' || arrowTop === 'calc(100% - 7px)') {
    const arrowCenter = (rect.left + rect.width / 2) - left;
    let clampedArrow = arrowCenter - 7;
    if (clampedArrow < 15) clampedArrow = 15;
    if (clampedArrow > popupRect.width - 29) clampedArrow = popupRect.width - 29;
    arrowLeft = clampedArrow + 'px';
  } else {
    const arrowCenterY = (rect.top + rect.height / 2) - top;
    let clampedArrowY = arrowCenterY - 7;
    if (clampedArrowY < 15) clampedArrowY = 15;
    if (clampedArrowY > popupRect.height - 29) clampedArrowY = popupRect.height - 29;
    arrowTop = clampedArrowY + 'px';
  }
  
  tutPopup.style.top = top + 'px';
  tutPopup.style.left = left + 'px';
  
  tutArrow.style.top = arrowTop;
  tutArrow.style.left = arrowLeft;
}

function finishTutorial() {
  localStorage.setItem('tutFinished', 'true');
  tutOverlay.classList.add('hidden');
  tutPopup.classList.add('hidden');
  
  // Re-enable scroll
  document.body.classList.remove('tutorial-active');
  const main = document.querySelector('.broker-main');
  if (main) main.classList.remove('tutorial-active');

  if (window.tutPrevTarget) {
    window.tutPrevTarget.style.zIndex = '';
    
    if (window.tutPrevTarget.dataset.tutOldPos) {
      window.tutPrevTarget.style.position = window.tutPrevTarget.dataset.tutOldPos;
      delete window.tutPrevTarget.dataset.tutOldPos;
    } else {
      window.tutPrevTarget.style.position = '';
    }
    
    if (window.tutPrevTarget.dataset.tutOldPointer !== undefined) {
      window.tutPrevTarget.style.pointerEvents = window.tutPrevTarget.dataset.tutOldPointer;
      delete window.tutPrevTarget.dataset.tutOldPointer;
    } else {
      window.tutPrevTarget.style.pointerEvents = '';
    }
    
    window.tutPrevTarget = null;
  }

  if (tutActiveTarget) {
    tutActiveTarget = null;
  }
  
  const buyPanel = document.getElementById('buy-panel');
  if (buyPanel) {
    if (buyPanel.dataset.tutMock === 'true') {
      delete buyPanel.dataset.tutMock;
      if (typeof closeBuyPanel === 'function') closeBuyPanel();
    }
    if (typeof state !== 'undefined' && !state.selectedAsset) {
      buyPanel.classList.remove('visible');
    }
  }

  if (document.body.dataset.tutPortMock === 'true') {
    delete document.body.dataset.tutPortMock;
    delete state.portfolio['VALE3'];
    if (typeof renderPortfolio === 'function') renderPortfolio();
  }

  const portSummary = document.getElementById('portfolio-summary');
  if (portSummary && portSummary.dataset.tutForced === 'true') {
    if (typeof state !== 'undefined' && Object.keys(state.portfolio || {}).length === 0) {
      portSummary.style.display = 'none';
    }
    delete portSummary.dataset.tutForced;
  }

  const finalOverlay = document.getElementById('final-overlay');
  if (finalOverlay && finalOverlay.dataset.tutForced === 'true') {
    finalOverlay.classList.remove('active');
    delete finalOverlay.dataset.tutForced;
  }
}

window.addEventListener('resize', () => {
  if (!tutPopup.classList.contains('hidden') && tutActiveTarget) {
    positionTutorialPopup(tutorialSteps[tutCurrentStep]);
  }
});

// Suspense Overlay Logic
const suspenseOverlay = document.getElementById('suspense-overlay');
const btnSuspenseYes = document.getElementById('btn-suspense-yes');
const btnSuspenseNo = document.getElementById('btn-suspense-no');
const suspenseActions = document.getElementById('suspense-actions');
const suspenseLoader = document.getElementById('suspense-loader');

if (suspenseOverlay) {
  const hasSeenSuspense = localStorage.getItem('suspense_seen');
  
  if (hasSeenSuspense === 'true') {
    // Already saw it, hide immediately and start
    suspenseOverlay.style.display = 'none';
    initTutorial();
  } else {
    // Show overlay
    document.body.style.overflow = 'hidden';
    
    // No button
    if (btnSuspenseNo) {
      btnSuspenseNo.addEventListener('click', () => {
        window.location.href = '../index.html#aprender';
      });
    }
    
    // Yes button
    if (btnSuspenseYes) {
      btnSuspenseYes.addEventListener('click', () => {
        // Hide buttons, show loader, start animation
        if(suspenseActions) suspenseActions.style.display = 'none';
        if(suspenseLoader) suspenseLoader.style.display = 'block';
        
        // Trigger reflow to ensure display block is registered before adding class
        void suspenseLoader.offsetWidth;
        
        suspenseOverlay.classList.add('loading-active');
        
        setTimeout(() => {
          suspenseOverlay.classList.add('fade-out');
          document.body.style.overflow = '';
          localStorage.setItem('suspense_seen', 'true');
          
          setTimeout(() => {
            suspenseOverlay.style.display = 'none';
            initTutorial();
          }, 800);
        }, 5000);
      });
    }
  }
} else {
  initTutorial();
}

